package assignment;

public class Java_pattern_program {
	public static void main(String[] args) {
		for(int i=1; i<=5;i++) {
			for(int j=1;j<=i;j++) {
				System.out.print("*");
			}
		System.out.println(" ");
		}
		for (int k=3;k<=0;k--) {
			for(int m=0; m<=k; m++) {
				System.out.print("* ");
			}
		System.out.println(" ");
		}
		

	}

}